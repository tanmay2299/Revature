package switch_cases;

public class SwitchCase {
	public static void main(String[] args) {
		
			    int city = 4;
			    switch (city) {
			      case 1:
			        System.out.println("Pune");
			        break;
			      case 2:
			        System.out.println("Mumbai");
			        break;
			      case 3:
			        System.out.println("Delhi");
			        break;
			      case 4:
			        System.out.println("Banglore");
			        break;
			      case 5:
			        System.out.println("Goa");
			        break;
			     
			    }
			  

	}

}
